function completeness(partial: Partial<CoffeeShop>): DataCompleteness {
  const fields = {
    hasHeroImage: !!partial.heroImage,
    hasDescription: !!(partial.description && partial.description.length > 20),
    hasAddress: !!partial.address,
    hasCoords: !!(partial.latitude && partial.longitude),
    hasBrewAttributes: partial.hasEspresso !== undefined,
    hasTags: !!(partial.tags && partial.tags.length > 0),
    hasProReviews: !!(partial.proReviewCount && partial.proReviewCount > 0),
  };
  const score = Object.values(fields).filter(Boolean).length / Object.keys(fields).length;
  return { ...fields, score };
}

function makeShop(
  partial: Omit<CoffeeShop, "communityScore" | "proScore" | "moderationStatus" | "source" | "dataCompleteness"> & {
    proCriteriaAverages: CoffeeShop["proCriteriaAverages"];
  }
): CoffeeShop {
  const communityScore = eloToScore(partial.communityElo);
  const proScore = partial.proCriteriaAverages
    ? calculateProScore(partial.proCriteriaAverages)
    : null;
  return {
    ...partial,
    communityScore,
    proScore,
    moderationStatus: "approved",
    source: MANUAL_SOURCE,
    dataCompleteness: completeness(partial),
  };
}

