export const NYC_SHOPS = SHOPS.filter((s) => s.city === "New York");
export const LA_SHOPS = SHOPS.filter((s) => s.city === "Los Angeles");

export function getShopBySlug(slug: string): CoffeeShop | undefined {
  return SHOPS.find((s) => s.slug === slug);
}

export function getShopById(id: string): CoffeeShop | undefined {
  return SHOPS.find((s) => s.id === id);
}

export function getFeaturedShops(): CoffeeShop[] {
  return SHOPS.filter((s) => s.featured);
}

export function getTrendingShops(): CoffeeShop[] {
  return SHOPS.filter((s) => s.trending);
}

export function getRecentlyAdded(): CoffeeShop[] {
  return SHOPS.filter((s) => s.recentlyAdded);
}

export function getTopRankedByCity(city: string, limit = 10): CoffeeShop[] {
  return SHOPS.filter((s) => s.city === city)
    .sort((a, b) => b.communityScore - a.communityScore)
    .slice(0, limit);
}

export function getBestEspresso(limit = 5): CoffeeShop[] {
  return SHOPS.filter(
    (s) => s.hasEspresso && s.proCriteriaAverages?.qualityOfEspresso !== null
  )
    .sort((a, b) => {
      const aScore = a.proCriteriaAverages?.qualityOfEspresso ?? 0;
      const bScore = b.proCriteriaAverages?.qualityOfEspresso ?? 0;
      return bScore - aScore;
    })
    .slice(0, limit);
}

export function getBestPourOver(limit = 5): CoffeeShop[] {
  return SHOPS.filter(
    (s) => s.hasPourOver && s.proCriteriaAverages?.qualityOfPourOver !== null
  )
    .sort((a, b) => {
      const aScore = a.proCriteriaAverages?.qualityOfPourOver ?? 0;
      const bScore = b.proCriteriaAverages?.qualityOfPourOver ?? 0;
      return bScore - aScore;
    })
    .slice(0, limit);
}

export function getBestVibe(limit = 5): CoffeeShop[] {
  return SHOPS.filter((s) => s.proCriteriaAverages)
    .sort((a, b) => {
      const aScore = a.proCriteriaAverages?.atmosphereVibe ?? 0;
      const bScore = b.proCriteriaAverages?.atmosphereVibe ?? 0;
      return bScore - aScore;
    })
    .slice(0, limit);
}
